int
parse_internal (int argc, char * const *argv, struct gengetopt_args_info *args_info, int override, int initialize, int check_required, const char *additional_error)
{
  int c;	/* Character of the parsed option.  */
  char *multi_token, *multi_next; /* for multiple options */

  int i;        /* Counter */

  struct string_list * file_list = NULL,* file_new = NULL;
  struct string_list * include_list = NULL,* include_new = NULL;
  int error = 0;
  struct gengetopt_args_info local_args_info;

  if (initialize)
    parse_init (args_info);

  parse_init (&local_args_info);

  optarg = 0;
  optind = 0;
  opterr = 1;
  optopt = '?';

  while (1)
    {
      int option_index = 0;
      char *stop_char;

      static struct option long_options[] = {
        { "help",	0, NULL, 'h' },
        { "version",	0, NULL, 'V' },
        { "source",	1, NULL, 's' },
        { "out",	1, NULL, 'o' },
        { "out-c",	1, NULL, 'c' },
        { "out-h",	1, NULL, 'r' },
        { "file",	1, NULL, 'f' },
        { "prefix",	1, NULL, 'p' },
        { "include",	1, NULL, 'i' },
        { "contentlength",	0, NULL, 'l' },
        { "nostd",	0, NULL, 'n' },
        { "nocgix",	0, NULL, 0 },
        { "userarg",	0, NULL, 'u' },
        { "extended",	0, NULL, 'x' },
        { "module",	0, NULL, 'm' },
        { NULL,	0, NULL, 0 }
      };

      stop_char = 0;
      c = getopt_long (argc, argv, "hVs:o:c:r:f:p:i:lnuxm", long_options, &option_index);

      if (c == -1) break;	/* Exit from `while (1)' loop.  */

      switch (c)
        {
        case 'h':	/* Print help and exit.  */
          parse_print_help ();
          parse_free (&local_args_info);
          exit (EXIT_SUCCESS);

        case 'V':	/* Print version and exit.  */
          parse_print_version ();
          parse_free (&local_args_info);
          exit (EXIT_SUCCESS);

        case 's':	/* source filename (*.q) for processing.  */
          if (local_args_info.source_given)
            {
              fprintf (stderr, "%s: `--source' (`-s') option given more than once%s\n", argv[0], (additional_error ? additional_error : ""));
              goto failure;
            }
          if (args_info->source_given && ! override)
            continue;
          local_args_info.source_given = 1;
          args_info->source_given = 1;
          if (args_info->source_arg)
            free (args_info->source_arg); /* free previous string */
          args_info->source_arg = gengetopt_strdup (optarg);
          if (args_info->source_orig)
            free (args_info->source_orig); /* free previous string */
          args_info->source_orig = gengetopt_strdup (optarg);
          break;

        case 'o':	/* project name (default source filename before dot).  */
          if (local_args_info.out_given)
            {
              fprintf (stderr, "%s: `--out' (`-o') option given more than once%s\n", argv[0], (additional_error ? additional_error : ""));
              goto failure;
            }
          if (args_info->out_given && ! override)
            continue;
          local_args_info.out_given = 1;
          args_info->out_given = 1;
          if (args_info->out_arg)
            free (args_info->out_arg); /* free previous string */
          args_info->out_arg = gengetopt_strdup (optarg);
          if (args_info->out_orig)
            free (args_info->out_orig); /* free previous string */
          args_info->out_orig = gengetopt_strdup (optarg);
          break;

        case 'c':	/* result filename (*.c).  */
          if (local_args_info.out_c_given)
            {
              fprintf (stderr, "%s: `--out-c' (`-c') option given more than once%s\n", argv[0], (additional_error ? additional_error : ""));
              goto failure;
            }
          if (args_info->out_c_given && ! override)
            continue;
          local_args_info.out_c_given = 1;
          args_info->out_c_given = 1;
          if (args_info->out_c_arg)
            free (args_info->out_c_arg); /* free previous string */
          args_info->out_c_arg = gengetopt_strdup (optarg);
          if (args_info->out_c_orig)
            free (args_info->out_c_orig); /* free previous string */
          args_info->out_c_orig = gengetopt_strdup (optarg);
          break;

        case 'r':	/* result header filename (*.h).  */
          if (local_args_info.out_h_given)
            {
              fprintf (stderr, "%s: `--out-h' (`-r') option given more than once%s\n", argv[0], (additional_error ? additional_error : ""));
              goto failure;
            }
          if (args_info->out_h_given && ! override)
            continue;
          local_args_info.out_h_given = 1;
          args_info->out_h_given = 1;
          if (args_info->out_h_arg)
            free (args_info->out_h_arg); /* free previous string */
          args_info->out_h_arg = gengetopt_strdup (optarg);
          if (args_info->out_h_orig)
            free (args_info->out_h_orig); /* free previous string */
          args_info->out_h_orig = gengetopt_strdup (optarg);
          break;

        case 'f':	/* internal file for writing cached output.  */
          local_args_info.file_given++;
        
          multi_token = get_multiple_arg_token(optarg);
          multi_next = get_multiple_arg_token_next (optarg);
        
          while (1)
            {
              file_new = (struct string_list *) malloc (sizeof (struct string_list));
              file_new->next = file_list;
              file_list = file_new;
              file_new->arg = gengetopt_strdup (multi_token);
              file_new->orig = multi_token;
        
              if (multi_next)
                {
                  multi_token = get_multiple_arg_token(multi_next);
                  multi_next = get_multiple_arg_token_next (multi_next);
                  local_args_info.file_given++;
                }
              else
                break;
            }
          break;

        case 'p':	/* function prefix (like qtp_).  */
          if (local_args_info.prefix_given)
            {
              fprintf (stderr, "%s: `--prefix' (`-p') option given more than once%s\n", argv[0], (additional_error ? additional_error : ""));
              goto failure;
            }
          if (args_info->prefix_given && ! override)
            continue;
          local_args_info.prefix_given = 1;
          args_info->prefix_given = 1;
          if (args_info->prefix_arg)
            free (args_info->prefix_arg); /* free previous string */
          args_info->prefix_arg = gengetopt_strdup (optarg);
          if (args_info->prefix_orig)
            free (args_info->prefix_orig); /* free previous string */
          args_info->prefix_orig = gengetopt_strdup (optarg);
          break;

        case 'i':	/* include .h file in template.  */
          local_args_info.include_given++;
        
          multi_token = get_multiple_arg_token(optarg);
          multi_next = get_multiple_arg_token_next (optarg);
        
          while (1)
            {
              include_new = (struct string_list *) malloc (sizeof (struct string_list));
              include_new->next = include_list;
              include_list = include_new;
              include_new->arg = gengetopt_strdup (multi_token);
              include_new->orig = multi_token;
        
              if (multi_next)
                {
                  multi_token = get_multiple_arg_token(multi_next);
                  multi_next = get_multiple_arg_token_next (multi_next);
                  local_args_info.include_given++;
                }
              else
                break;
            }
          break;

        case 'l':	/* Insert 'Content-Length:' tag.  */
          if (local_args_info.contentlength_given)
            {
              fprintf (stderr, "%s: `--contentlength' (`-l') option given more than once%s\n", argv[0], (additional_error ? additional_error : ""));
              goto failure;
            }
          if (args_info->contentlength_given && ! override)
            continue;
          local_args_info.contentlength_given = 1;
          args_info->contentlength_given = 1;
          break;

        case 'n':	/* no std output from Q-Lang cgi daemon.  */
          if (local_args_info.nostd_given)
            {
              fprintf (stderr, "%s: `--nostd' (`-n') option given more than once%s\n", argv[0], (additional_error ? additional_error : ""));
              goto failure;
            }
          if (args_info->nostd_given && ! override)
            continue;
          local_args_info.nostd_given = 1;
          args_info->nostd_given = 1;
          break;

        case 'u':	/* Enable passing of user argument.  */
          if (local_args_info.userarg_given)
            {
              fprintf (stderr, "%s: `--userarg' (`-u') option given more than once%s\n", argv[0], (additional_error ? additional_error : ""));
              goto failure;
            }
          if (args_info->userarg_given && ! override)
            continue;
          local_args_info.userarg_given = 1;
          args_info->userarg_given = 1;
          break;

        case 'x':	/* Use <!--- (three minus) as delimiter.  */
          if (local_args_info.extended_given)
            {
              fprintf (stderr, "%s: `--extended' (`-x') option given more than once%s\n", argv[0], (additional_error ? additional_error : ""));
              goto failure;
            }
          if (args_info->extended_given && ! override)
            continue;
          local_args_info.extended_given = 1;
          args_info->extended_given = 1;
          break;

        case 'm':	/* Include QUERY-module in source.  */
          if (local_args_info.module_given)
            {
              fprintf (stderr, "%s: `--module' (`-m') option given more than once%s\n", argv[0], (additional_error ? additional_error : ""));
              goto failure;
            }
          if (args_info->module_given && ! override)
            continue;
          local_args_info.module_given = 1;
          args_info->module_given = 1;
          break;


        case 0:	/* Long option with no short option */
          /* No CGIX support enabled.  */
          if (strcmp (long_options[option_index].name, "nocgix") == 0)
          {
            if (local_args_info.nocgix_given)
              {
                fprintf (stderr, "%s: `--nocgix' option given more than once%s\n", argv[0], (additional_error ? additional_error : ""));
                goto failure;
              }
            if (args_info->nocgix_given && ! override)
              continue;
            local_args_info.nocgix_given = 1;
            args_info->nocgix_given = 1;
            break;
          }
          
          break;
        case '?':	/* Invalid option.  */
          /* `getopt_long' already printed an error message.  */
          goto failure;

        default:	/* bug: option not considered.  */
          fprintf (stderr, "%s: option unknown: %c%s\n", PARSE_PACKAGE, c, (additional_error ? additional_error : ""));
          abort ();
        } /* switch */
    } /* while */


  if (local_args_info.file_given && file_list)
    {
      struct string_list *tmp;
      args_info->file_arg = (char * *) realloc (args_info->file_arg, (args_info->file_given + local_args_info.file_given) * sizeof (char *));
      args_info->file_orig = (char **) realloc (args_info->file_orig, (args_info->file_given + local_args_info.file_given) * sizeof (char *));
      for (i = (local_args_info.file_given - 1); i >= 0; --i)
        {
          tmp = file_list;
          args_info->file_arg [i + args_info->file_given] = file_list->arg;
          args_info->file_orig [i + args_info->file_given] = file_list->orig;
          file_list = file_list->next;
          free (tmp);
        }
    }
  
  if (local_args_info.include_given && include_list)
    {
      struct string_list *tmp;
      args_info->include_arg = (char * *) realloc (args_info->include_arg, (args_info->include_given + local_args_info.include_given) * sizeof (char *));
      args_info->include_orig = (char **) realloc (args_info->include_orig, (args_info->include_given + local_args_info.include_given) * sizeof (char *));
      for (i = (local_args_info.include_given - 1); i >= 0; --i)
        {
          tmp = include_list;
          args_info->include_arg [i + args_info->include_given] = include_list->arg;
          args_info->include_orig [i + args_info->include_given] = include_list->orig;
          include_list = include_list->next;
          free (tmp);
        }
    }
  

  args_info->file_given += local_args_info.file_given;
  local_args_info.file_given = 0;
  args_info->include_given += local_args_info.include_given;
  local_args_info.include_given = 0;
  
  if (check_required)
    {
      error += parse_required2 (args_info, argv[0], additional_error);
    }

  parse_release (&local_args_info);

  if ( error )
    return (EXIT_FAILURE);

  return 0;

failure:
  if (file_list)
    {
      struct string_list *tmp;
      while (file_list)
        {
          tmp = file_list;
          free (file_list->arg);
          free (file_list->orig);
          file_list = file_list->next;
          free (tmp);
        }
    }
  if (include_list)
    {
      struct string_list *tmp;
      while (include_list)
        {
          tmp = include_list;
          free (include_list->arg);
          free (include_list->orig);
          include_list = include_list->next;
          free (tmp);
        }
    }
  
  parse_release (&local_args_info);
  return (EXIT_FAILURE);
}
